// switch - case 

// switch (key) {
//     case value:
//         // execute this block if key === value
//         break;
//     default:
//         // execute this block if no case matches
//         break;
// }

const month = 2 // "Feb"

switch (month) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("February");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    default:
        console.log("Default Case Match!");
        break;
}
